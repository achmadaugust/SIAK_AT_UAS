package sysinfoakademikat;

/**
 *
 * @author AT
 */
public class Nilai {
    private int id;
    private int npm;
    private String nama;
    private int idMk;
    private String namaMk;
    private int sks;
    private int nAngka;
    private String nHuruf;

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }
    
    public int getNpm() {
        return npm;
    }

    public void setNpm(int npm) {
        this.npm = npm;
    }

    public String getNama() {
        return nama;
    }

    public void setNama(String nama) {
        this.nama = nama;
    }

    public int getIdMk() {
        return idMk;
    }

    public void setIdMk(int idMk) {
        this.idMk = idMk;
    }

    public String getNamaMk() {
        return namaMk;
    }

    public void setNamaMk(String namaMk) {
        this.namaMk = namaMk;
    }

    public int getSks() {
        return sks;
    }

    public void setSks(int sks) {
        this.sks = sks;
    }

    public int getnAngka() {
        return nAngka;
    }

    public void setnAngka(int nAngka) {
        this.nAngka = nAngka;
    }

    public String getnHuruf() {
        return nHuruf;
    }

    public void setnHuruf(String nHuruf) {
        this.nHuruf = nHuruf;
    }
    
}
